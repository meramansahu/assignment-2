import java.util.Scanner;
class q2if{
public static void main(String[] args){
Scanner sc1=new Scanner(System.in);
Scanner sc2=new Scanner(System.in);
int a=sc1.nextInt();
int b=sc2.nextInt();
if(a>b)
{
System.out.println(a+" "+"is greater");
}
else
{
System.out.println(b+" "+"is greater");
}
}
}